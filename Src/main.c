  delay = 20;
  while(delay--);
  delay = 2000;
  HAL_I2C_Master_Receive(&hi2c1, devReadAddr, i2cTempData, 1, timeOut);
  HAL_UART_Transmit(&huart1, i2cTempData, 1, timeOut);
//  HAL_I2C_Master_Receive(&hi2c1, (uint16_t)devAddr, i2cData, (uint16_t)size, (uint32_t)timeOut);
